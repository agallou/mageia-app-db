				if (!$this->aNewVersionRequest->validate($columns)) {
					$failureMap = array_merge($failureMap, $this->aNewVersionRequest->getValidationFailures());
				}
			}


			if (($retval = UserHasNewVersionRequestPeer::doValidate($this, $columns)) !== true) {
				$failureMap = array_merge($failureMap, $retval);
			}



			$this->alreadyInValidation = false;
		}

		return (!empty($failureMap) ? $failureMap : true);
	}

